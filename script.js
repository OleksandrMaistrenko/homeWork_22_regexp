// Написати регулярний вираз, який перевіряє, чи є рядок 6-значним числом:

// '   123456  ' -> так
// ' -0123456  ' -> так
// '     -12345  ' -> ні
// '   -023456' -> ні
// ' -000016   ' -> ні
// ' -000000123456  ' -> так
// ' - 000000123456  ' -> ні
// ' +000123456  ' -> так

let checkedValue = " -000000123456  ";

const regexp = /^\s*[+-]?(?:0*[1-9]{6})\s*$/;

const result = checkedValue.match(regexp);

console.log(result[0]);
